HF_TOKEN = os.environ.get('HF_TOKEN', '')
HF_ENDPOINTS = [
    {'url': 'https://router.huggingface.co/sambanova/v1/chat/completions', 'model': 'Meta-Llama-3.3-70B-Instruct'},
    {'url': 'https://router.huggingface.co/novita/v3/openai/chat/completions', 'model': 'meta-llama/llama-3.3-70b-instruct'},
    {'url': 'https://router.huggingface.co/cerebras/v1/chat/completions', 'model': 'llama-3.3-70b'},
]


def call_llm(messages):
    """Вызывает LLM через HuggingFace — пробует несколько провайдеров."""
    headers = {
        'Authorization': f'Bearer {HF_TOKEN}',
        'Content-Type': 'application/json',
    }

    last_error = None
    for ep in HF_ENDPOINTS:
        payload = {
            'model': ep['model'],
            'messages': messages,
            'max_tokens': 600,
            'temperature': 0.7,
        }
        try:
            resp = requests.post(ep['url'], json=payload, headers=headers, timeout=25)
            if resp.status_code == 200:
                data = resp.json()
                return data['choices'][0]['message']['content']
            last_error = f"{ep['url']}: {resp.status_code} {resp.text[:200]}"
        except Exception as e:
            last_error = f"{ep['url']}: {str(e)}"

    raise Exception(f'All HF endpoints failed. Last: {last_error}')


def handler(event, context):
    """Обрабатывает запросы к AI-чату MOSPOTOLKI."""
    if event.get('httpMethod') == 'OPTIONS':
        return {
            'statusCode': 200,
            'headers': {
                'Access-Control-Allow-Origin': '*',
                'Access-Control-Allow-Methods': 'GET, POST, PUT, DELETE, OPTIONS',
                'Access-Control-Allow-Headers': 'Content-Type, X-User-Id, X-Auth-Token, X-Session-Id',
                'Access-Control-Max-Age': '86400',
            },
            'body': '',
        }

    cors = {'Access-Control-Allow-Origin': '*', 'Content-Type': 'application/json'}

    if event.get('httpMethod') != 'POST':
        return {'statusCode': 405, 'headers': cors, 'body': json.dumps({'error': 'Method not allowed'})}

    body = json.loads(event.get('body', '{}'))
    messages = body.get('messages', [])

    if not messages:
        return {'statusCode': 400, 'headers': cors, 'body': json.dumps({'error': 'No messages provided'})}

    openai_messages = [{'role': 'system', 'content': SYSTEM_PROMPT}]
    for msg in messages[-10:]:
        openai_messages.append({
            'role': msg.get('role', 'user'),
            'content': msg.get('text', ''),
        })

    answer = call_llm(openai_messages)

    return {
        'statusCode': 200,
        'headers': cors,
        'body': json.dumps({'answer': answer}),
    }